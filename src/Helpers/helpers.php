if (!function_exists('nw_lastName')) {
    function nw_lastName($fullname, &$name = null)
    {
        $lastName = '';
        $e = explode(' ', $fullname);
        if (count($e) > 1) {
            $lastName = ucfirst($e[count($e) - 1]);
            unset($e[count($e) - 1]);
        }

        $name = implode(' ', $e);
        $name = ucwords($name);

        return $lastName;
    }
}

if (!function_exists('nw_mask')) {
    function nw_mask($val, $mask)
    {
        if (empty($val)) {
            return '';
        }

        $maskared = '';
        $k = 0;
        for ($i = 0; $i <= strlen($mask) - 1; $i++) {
            if ($mask[$i] == '#') {
                if (isset($val[$k])) {
                    $maskared .= $val[$k++];
                }
            } else {
                if (isset($mask[$i])) {
                    $maskared .= $mask[$i];
                }
            }
        }

        return $maskared;
    }
}

